radius = 2
PI = 3.14159

diameter = 2 * radius
print('The diameter is: ', diameter)

circumference = 2 * (PI * radius)
rounded_circumference = round(circumference, 2)
print('The circumference is: ', rounded_circumference)

area = PI * (radius ** 2)
rounded_area = round(area, 2)
print('The area is: ', rounded_area)